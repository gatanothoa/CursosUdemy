# Tipos de datos en Python

# Enteros (int)
numero_entero = 10

# Flotantes (float)
numero_flotante = 3.14

# Cadenas de texto (str)
texto = "Hola, mundo"

# Booleanos (bool)
es_verdadero = True
es_falso = False

# Listas (list)
lista = [1, 2, 3, "cuatro"]

# Tuplas (tuple)
tupla = (1, 2, 3)

# Conjuntos (set)
conjunto = {1, 2, 3}

# Diccionarios (dict)
diccionario = {"nombre": "Ana", "edad": 25}

# Imprimir los tipos de datos
print(type(numero_entero))
print(type(numero_flotante))
print(type(texto))
print(type(es_verdadero))
print(type(lista))
print(type(tupla))
print(type(conjunto))
print(type(diccionario))