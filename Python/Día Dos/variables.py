#Nombres de las variables

# Reglas para los nombres de las variables en Python:
# 1. Deben comenzar con una letra (a-z, A-Z) o un guion bajo (_).
# 2. El resto del nombre puede contener letras, números (0-9) y guiones bajos.
# 3. No pueden comenzar con un número.
# 4. No pueden contener espacios ni caracteres especiales como @, $, %, etc.
# 5. Python distingue entre mayúsculas y minúsculas (nombre y Nombre son diferentes).
# 6. No se deben usar palabras reservadas del lenguaje como nombres de variables.
