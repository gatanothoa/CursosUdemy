# Variables
# Las variables son contenedores para almacenar datos.
# En Python, no es necesario declarar el tipo de variable.
# Se puede asignar un valor a una variable utilizando el signo igual (=).

nombre = "Antonio"
apellido = "Córdova"
edad = 40
edad1 = 1


print("Nombre:", nombre)
print("Apellido:", apellido)
print("Edad:", edad)
print(edad + edad1)

print(f"Tu nombre es {nombre} {apellido} y tienes {edad} años.")

personaje = input("¿Cuál es tu personaje favorito? ")
print(f"Tu personaje favorito es {personaje}.")

