#Operadores Matemáticos

# Los operadores matemáticos en Python permiten realizar operaciones aritméticas.
# Ejemplos de operadores:
# Suma (+), Resta (-), Multiplicación (*), División (/), División entera (//), Módulo (%), Potencia (**)

a = 10
b = 3

print("Suma:", a + b)           # 13
print("Resta:", a - b)          # 7
print("Multiplicación:", a * b) # 30
print("División:", a / b)       # 3.333...
print("División entera:", a // b) # 3
print("Módulo:", a % b)         # 1
print("Potencia:", a ** b)      # 1000