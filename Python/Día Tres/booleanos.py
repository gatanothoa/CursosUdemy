# Los booleanos son un tipo de dato que solo puede tener dos valores: True o False.
# Se utilizan para realizar comparaciones y tomar decisiones en el código.
# Los operadores de comparación devuelven un valor booleano.

a = 5
b = 10
c = 5

print(a == b) # Igual a False
print(a == c) # Igual a True
print(a < b) # Menor que True
print(a > b) # Mayor que False
print(a <= c) # Menor o igual que True
print(a >= c) # Mayor o igual que True

